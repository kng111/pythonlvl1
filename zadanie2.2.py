# Вводим начальное расстояние и текущее расстояние
initial_distance = int(input("Введите начальное расстояние: "))
current_distance = initial_distance

# Вводим возможность лягушке отпрыгнуть назад
can_jump_back = input("Лягушка может отпрыгнуть назад? (да/нет): ").lower() == 'да'

# Количество чашек кофе и текущий прогресс
cups_of_coffee = 0
progress = 0

# Лягушка "пьет кофе" и прыгает, пока не допрыгает до второго листа
while progress < initial_distance:
    cups_of_coffee += 1
    progress += 1

    # Проверяем, не вернулась ли лягушка обратно
    if can_jump_back and progress < current_distance - 0.5:
        progress = current_distance
        cups_of_coffee += 1

        # Она вернулась на полметра назад
        progress -= 0.5

    # Пересчитываем расстояние после каждого прыжка
    current_distance = initial_distance - progress

# Выводим результат
print(f"Лягушке понадобилось {cups_of_coffee} чашек кофе, чтобы доплыть до второго листа!")



"""В этой задаче мы имеем лягушку, которая сидит на первом листе в озере и хочет доплыть до второго листа. Расстояние между листьями изначально задается пользователем. Лягушка имеет способность выпить чашку кофе и прыгнуть на 1 метр вперед.

Сначала мы определяем начальное расстояние и текущее расстояние (которое изначально равно начальному расстоянию). Затем у нас есть счетчик чашек кофе и переменная progress, которая представляет прогресс лягушки в метрах.

Лягушка начинает пить кофе и прыгать. Каждый раз, когда она прыгает, progress увеличивается на 1. Если включена возможность лягушке отпрыгнуть назад (can_jump_back равно True), мы проверяем, не вернулась ли лягушка обратно после прыжка. Если так, она выпивает еще одну чашку кофе и отпрыгивает на полметра назад.

Мы пересчитываем текущее расстояние после каждого прыжка, вычитая прогресс из начального расстояния.

Процесс продолжается, пока лягушка не допрыгает до второго листа. После этого программа выводит количество чашек кофе, которые лягушка выпила, чтобы достичь второго листа."""