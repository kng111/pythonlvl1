# Этап 1: Генерация случайного массива
import random

# Генерация случайного массива из 10 элементов в диапазоне от -10 до 10
random_list = [random.randint(-10, 10) for _ in range(10)]
print(f"Сгенерированный массив: {random_list}")

# Этап 2: Поиск подмассива с максимальной суммой
max_sum = float('-inf')  # Инициализация переменной для хранения максимальной суммы
current_sum = 0  # Инициализация переменной для хранения текущей суммы
start = end = 0  # Инициализация переменных для хранения начального и конечного индексов подмассива с максимальной суммой
current_start = 0  # Инициализация переменной для хранения начального индекса текущего подмассива

for i, num in enumerate(random_list):  # Перебор элементов массива с индексами
    current_sum += num  # Прибавление текущего числа к текущей сумме

    if current_sum > max_sum:  # Если текущая сумма больше максимальной
        max_sum = current_sum  # Обновление максимальной суммы
        start = current_start  # Обновление начального индекса подмассива с максимальной суммой
        end = i  # Обновление конечного индекса подмассива с максимальной суммой

    if current_sum < 0:  # Если текущая сумма отрицательная
        current_sum = 0  # Обнуление текущей суммы
        current_start = i + 1  # Обновление начального индекса текущего подмассива

max_subarray = random_list[start:end+1]  # Выделение подмассива с максимальной суммой

# Этап 3: Использование структур данных
print(f"Подмассив с максимальной суммой: {max_subarray}")


